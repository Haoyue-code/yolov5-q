def url2file(url):
    # Convert URL to filename, i.e. https://url.com/file.txt?auth -> file.txt
    url = str(Path(url)).replace(":/", "://")  # Pathlib turns :// -> :/
    file = Path(urllib.parse.unquote(url)).name.split("?")[
        0
    ]  # '%2F' to '/', split https://url.com/file.txt?auth
    return file


